                <h2>Quién Soy?</h2>
                <p>Gorgory es el proyecto personal de Rafael Ramirez, quien a través de melodías dulces y letras melancólicas busca expresar emociones que poco puede decir con palabras.</p>
                <p>Actualmente tiene veinte años, y viene componiendo desde 2016. Su primer ep “Memos” fue grabado en su habitación durante tiempos de cuarentena y reúne 6 canciones que representan distintos momentos de su vida envueltos en un sentimiento de melancolía a cuatro paredes.</p>
                <p>De la mano de Zebra Home Studio y la agencia peruana de management Los Bits, se encuentra trabajando en la producción de nuevo material. ""Mi Fa"" es su primer estreno en esta nueva etapa creativa y continúa la evolución de sus canciones mediante una instrumentación más completa y una letra basada en el videojuego ""Zelda: Breath of the wild"".</p>
